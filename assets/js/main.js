const doughChoiceImgs = document.querySelectorAll(".dough__choice__img")
const doughChange = document.querySelectorAll(".dough__text__change")

doughChoiceImgs.forEach(changeDough);

function changeDough(item) {
    item.addEventListener('click', function () {
        let currentBtn = item;
        let tabId = currentBtn.getAttribute('data-dough');
        let currentTab = document.querySelector(tabId);

        if (!currentBtn.classList.contains('active')) {
            doughChoiceImgs.forEach(function (item) {
                item.classList.remove('active');
            });

            doughChange.forEach(function (item) {
                item.classList.remove('active');
            });

            currentBtn.classList.add('active');
            currentTab.classList.add('active');
        }
    });
}


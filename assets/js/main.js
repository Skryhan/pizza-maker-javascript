function fillInAllIngridients(arrIngridients) {

    for (let i = 0; i < arrIngridients.length; i++) {
        let idParent;
        if (arrIngridients[i].type === 'sea') {
            idParent = document.querySelector('#seaIngredientsAll');
        } else if (arrIngridients[i].type === 'meat') {
            idParent = document.querySelector('#meatIngredientsAll');
        } else if (arrIngridients[i].type === 'veg') {
            idParent = document.querySelector('#vegIngredientsAll');
        } else if (arrIngridients[i].type === 'cheese') {
            idParent = document.querySelector('#cheeseIngredientsAll');
        }

        idParent.innerHTML += `
        <div class='oneIngridient' 
        data-price-small=${arrIngridients[i].priceSmall} data-price-mid=${arrIngridients[i].priceMid} 
        data-price-large=${arrIngridients[i].priceLarge} data-weight-small=${arrIngridients[i].weightSmall} 
        data-weight-mid=${arrIngridients[i].weightMid} data-weight-large=${arrIngridients[i].weightLarge} 
        data-type=${arrIngridients[i].type} data-img-pizza=${arrIngridients[i].imgOnPizza}>
        <img src=${arrIngridients[i].img}>
        <p>${arrIngridients[i].name}</p>
        <div class='textIngridient'>
        <p class='weightoneIngridient'>80 гр</p>
        <span 'priceoneIngridient'>2 руб</span>
        </div>
        </div>`
    }

}

fillInAllIngridients(arrIngridients);



const selectedIngredients = document.querySelector('.selectedIngridients')

const selectIng = () => {
    selectedIngredients.innerHTML = ''
    const ingredients = document.querySelectorAll('.oneIngridient.active')

    let allSelectedIngredients = []

    if (ingredients) {
        for (let igredient of ingredients) {
            allSelectedIngredients.push(igredient.outerHTML)
        }
        selectedIngredients.innerHTML = allSelectedIngredients.join('')
    }
}



const oneIngridients = document.querySelectorAll('.oneIngridient img')
const ingredientsAll = document.querySelectorAll('.current-pizza-item')

const addIngredients = (ingredients) => {
    const nodesArray = Array.from(ingredients)
    const ingredientsArray = Array.from(ingredientsAll)
        for (let node of ingredients) {
            node.addEventListener('click', event => {
                event.target.parentNode.classList.toggle('active')
                const index = nodesArray.indexOf(event.target)
                ingredientsArray[index].classList.toggle('active')
                calculateOrder()
                selectIng()
            })
        }
}

addIngredients(oneIngridients)



